let grid;
let gridSize = 18;
let cellHeight, cellWidth;
let cellX;
let cellY;

function setup() {
  if (windowWidth < windowHeight) {
    createCanvas(windowWidth*0.8, windowWidth*0.8);
  }
  else {
    createCanvas(windowHeight*0.8, windowHeight*0.8);
  }
  cellWidth = width/gridSize;
  cellHeight = height/gridSize;
  grid = level3;
  
}

function draw() {
  background(220);
  displayGrid();
}

function keyPressed() {
  if (key === "2") {
    grid = level2;
  }
  if (key === "3") {
    grid = level3;
  }
}

function mousePressed() {
  let cellWidth = width/gridSize;
  let cellHeight = height/gridSize;
  cellX = Math.floor(mouseX/cellWidth);
  cellY = Math.floor(mouseY/cellHeight);

  if (grid[cellY][cellX] === 1) {
    grid[cellY][cellX] = 2;
  }
  if (grid[cellY][cellX] === 0) {
    grid[cellY][cellX] = 3;
  }
}

function displayGrid() {
  for (let y=0; y<gridSize; y++) {
    for (let x=0; x<gridSize; x++) {
      strokeWeight(0.5);
      if (grid[y][x] === 2) {
        fill("black");
      }
      if (grid[y][x] === 1 || grid[y][x] === 0) {
        fill("white");
      }
      if (grid[y][x] === 3) {
        if (cellWidth > cellHeight) {
          textSize(cellWidth*0.75);
          textAlign(CENTER, CENTER);
          text(grid[y][x], x*cellWidth + cellWidth/2, y*cellHeight + cellHeight/2);
        }
        else if (cellWidth < cellHeight) {
          textSize(cellHeight*0.75);
          textAlign(CENTER, CENTER);
          text(grid[y][x], x*cellWidth + cellWidth/2, y*cellHeight + cellHeight/2);
        }
      }
      rect(x*cellWidth, y*cellHeight, cellWidth, cellHeight);
    }
  }
}